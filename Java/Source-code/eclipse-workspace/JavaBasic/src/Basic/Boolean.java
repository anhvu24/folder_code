package Basic;

public class Boolean {

	public static void main(String[] args) 
	{

		 // Khai báo một biến kiểu boolan
        boolean value = true ;
 
        
        if (value == true) 
            System.out.println("It's true");
        
        else
            System.out.println("It's false");
        
 System.out.println("----------------------------");

        // With boolean type, you can also write
        if (value) 
            System.out.println("It's true");
        
        else 
            System.out.println("It's false");
		
       
	}
}
