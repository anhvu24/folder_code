
public class ViDu {
	public static void main(String[] args) {
		int a = 5;
		boolean b = true;
		
		System.out.println("-a: " + -a);
		System.out.println("+a: " + +a);
		System.out.println("!b: " + !b);
		System.out.println("-----");
		
		System.out.println("a: " + a);
		System.out.println("++a: " + ++a);
		System.out.println("a++: " + a++);
		System.out.println("a: " + a);
		
	}
}
